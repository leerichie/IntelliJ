package GroupOne.Week01;

public class LoopDoWhile {
    public static void main(String[] args) {
        System.out.println("Lift off in T minus...");
        System.out.println();
        int counter = 5;
        do {
            System.out.println(counter);
            counter--;
        }
        while (counter>=1);
        System.out.println();
        System.out.println("Lift off");
    }
}
