package GroupTwo.PodstawyB1;

public class MyDate {
    private int day;
    private int month;
    private int year;

    public int addDay(int dayCount) {
        return this.day = day;
    }

    public int addMonth(int monthCount) {
        return this.month = month;
    }

    public int addYear(int yearCount) {
        return this.year = year;
    }

    public int getDay() {
        return day;
    }

    public int getMonth() {
        return month;
    }

    public int getYear() {
        return year;
    }
}